package com.rise_world.gematik.accesskeeper.common.exception;

public class CertificateServiceException extends RuntimeException {

    private final int code;
    private final String text;

    public CertificateServiceException(int code, String text) {
        super(text);
        this.code = code;
        this.text = text;
    }

    public int getCode() {
        return code;
    }

    public String getText() {
        return text;
    }
}
